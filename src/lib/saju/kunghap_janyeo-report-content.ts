export function isJanyeoKunghapChapterReady(
  content: Record<string, unknown> | null | undefined,
  chapter: number,
): boolean {
  if (!content) return false;
  const keys = JANYEO_KUNGHAP_CHAPTER_SECTIONS[chapter];
  if (!keys || keys.length === 0) return true;
  return keys.every((k) => {
    const v = content[k];
    if (v == null) return false;
    if (typeof v === "object") {
      const obj = v as Record<string, unknown>;
      if (Array.isArray(obj.paragraphs)) return obj.paragraphs.length > 0;
      if (Array.isArray(obj.items)) return obj.items.length > 0;
      if (Array.isArray(obj.tips)) return obj.tips.length > 0;
      if (Array.isArray(obj.keywords)) return obj.keywords.length > 0;
      if (typeof obj.score === "number") return true;
      if (typeof obj.label === "string") return obj.label.length > 0;
      if (typeof obj.desc === "string") return obj.desc.length > 0;
      if (typeof obj.yongsinEl === "string") return obj.yongsinEl.length > 0;
      if (typeof obj.intro === "string") return obj.intro.length > 0;
      if (typeof obj.strengthDesc === "string") return obj.strengthDesc.length > 0;
      if (typeof obj.patternType === "string") return obj.patternType.length > 0;
      if (typeof obj.personalityType === "string") return obj.personalityType.length > 0;
      if (typeof obj.bondType === "string")        return obj.bondType.length > 0;
      if (typeof obj.tier === "string")            return obj.tier.length > 0;
      if (typeof obj.viewType === "string")        return obj.viewType.length > 0;
      if (typeof obj.gapDesc === "string")         return obj.gapDesc.length > 0;
      if (Array.isArray(obj.bridgePoints))         return obj.bridgePoints.length > 0;
      if (Array.isArray(obj.emotionPattern))       return obj.emotionPattern.length > 0;
      if (typeof obj.tip === "string")             return obj.tip.length > 0;
      if (typeof obj.effect === "string")          return obj.effect.length > 0;
      if (typeof obj.note === "string")            return obj.note.length > 0;
      if (typeof obj.patternType === "string" && obj.paragraphs) return (obj.paragraphs as unknown[]).length > 0;
      if (typeof obj.closing === "string")         return obj.closing.length > 0;
      if (typeof obj.eduStyleType === "string")    return obj.eduStyleType.length > 0;
      if (typeof obj.caution === "string")         return obj.caution.length > 0;
      if (Array.isArray(obj.doList))               return obj.doList.length > 0;
      if (typeof obj.level === "string")           return obj.level.length > 0;
      if (Array.isArray(obj.crisisWindows))        return obj.crisisWindows.length > 0;
      if (Array.isArray(obj.triggers))             return obj.triggers.length > 0;
      if (typeof obj.healing === "string")         return obj.healing.length > 0;
      if (typeof obj.blessing === "string")        return obj.blessing.length > 0;
      if (Array.isArray(obj.advice))               return obj.advice.length > 0;
      if (Array.isArray(obj.visions))              return obj.visions.length > 0;
      if (Array.isArray(obj.roles))                return obj.roles.length > 0;
      if (typeof obj.closing === "string")         return obj.closing.length > 0;
    }
    return false;
  });
}

