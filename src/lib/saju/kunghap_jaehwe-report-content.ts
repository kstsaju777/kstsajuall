export function isJaehweKunghapChapterReady(
  content: Record<string, unknown> | null | undefined,
  chapter: number,
): boolean {
  if (!content) return false;
  const keys = JAEHWE_KUNGHAP_CHAPTER_SECTIONS[chapter];
  if (!keys || keys.length === 0) return true;
  return keys.every((k) => {
    const v = content[k];
    if (v == null) return false;
    if (typeof v === "object") {
      const obj = v as Record<string, unknown>;
      if (Array.isArray(obj.paragraphs)) return obj.paragraphs.length > 0;
      if (Array.isArray(obj.items))      return obj.items.length > 0;
      if (Array.isArray(obj.tips))       return obj.tips.length > 0;
      if (Array.isArray(obj.keywords))   return obj.keywords.length > 0;
      if (Array.isArray(obj.scenes))     return obj.scenes.length > 0;
      if (Array.isArray(obj.phases))     return obj.phases.length > 0;
      if (Array.isArray(obj.strengths))  return obj.strengths.length > 0;
      if (Array.isArray(obj.reasons))    return obj.reasons.length > 0;
      if (Array.isArray(obj.steps))      return obj.steps.length > 0;
      if (Array.isArray(obj.visions))    return obj.visions.length > 0;
      if (typeof obj.score   === "number") return true;
      if (typeof obj.label   === "string") return obj.label.length > 0;
      if (typeof obj.desc    === "string") return obj.desc.length > 0;
      if (typeof obj.intro   === "string") return obj.intro.length > 0;
      if (typeof obj.closing === "string") return obj.closing.length > 0;
      if (typeof obj.summaryDesc === "string") return obj.summaryDesc.length > 0;
    }
    return false;
  });
}

