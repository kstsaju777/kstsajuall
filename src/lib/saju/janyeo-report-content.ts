// ── 핵심 함수: 장별 프롬프트 빌더 ──
export function buildJanyeoChapterPrompt(
  chapter: number,
  input: {
    name: string;
    gender: "male" | "female";
    manseryeokText: string;
    pillars?: { pos: string; gan: string; ganEl: string; ji: string; jiEl: string; sipTop: string; sipBot: string; sinsal?: string }[];
    birthYear?: number;
    seun?: { label: string; gz: string }[];
    daeun?: { label: string; gz: string; yearStart: number; active: boolean }[];
  }
): { system: string; user: string } {
  const theme = JANYEO_CH_THEME[chapter] ?? `[제${chapter}장]`;
  const guide = JANYEO_CH_GUIDE[chapter] ?? "";
  const schema = JANYEO_CH_SCHEMA[chapter] ?? "{}";
  const givenName = input.name.length > 1 ? input.name.slice(1) : input.name;
  const honor = givenName
    ? input.gender === "female" ? `${givenName}양` : `${givenName}군`
    : "이 아이";
  const currentYear = new Date().getFullYear();

  // 3장·5장: 오행 카운트 사전 계산
  let ohaengCountNote = "";
  if ((chapter === 3 || chapter === 5) && input.pillars && input.pillars.length > 0) {
    const cnt: Record<string, number> = { 목: 0, 화: 0, 토: 0, 금: 0, 수: 0 };
    for (const p of input.pillars) {
      if (p.ganEl && cnt[p.ganEl] !== undefined) cnt[p.ganEl]++;
      if (p.jiEl  && cnt[p.jiEl]  !== undefined) cnt[p.jiEl]++;
    }
    const total = Object.values(cnt).reduce((a, b) => a + b, 0) || 1;
    const sorted = Object.entries(cnt).sort((a, b) => b[1] - a[1]);
    const strong = sorted.filter(([, v]) => v >= 2).map(([k, v]) => `${k}(${v}개, ${Math.round(v/total*100)}%)`).join("·");
    const weak   = sorted.filter(([, v]) => v <= 1).map(([k, v]) => `${k}(${v}개, ${Math.round(v/total*100)}%)`).join("·");
    ohaengCountNote = `\n[오행 실제 개수 — 반드시 이 값 그대로 사용하시오]\n강한 오행(2개 이상): ${strong || "없음"}\n약한 오행(0~1개): ${weak || "없음"}\n이 수치와 다르게 서술하는 것은 절대 금지.\n`;
  }

  // 6장: 세운(2025~2034) + 대운 + 십성 주입
  let seunDaeunNote = "";
  if ((chapter === 6 || chapter === 7) && input.seun && input.seun.length > 0) {
    const seunLines = (input.seun as { label: string; gz: string; krName?: string; sipTop?: string; sipBot?: string }[])
      .filter(s => { const y = Number(s.label); return y >= 2025 && y <= 2034; })
      .map(s => `${s.label}년 ${s.krName ?? s.gz} — 천간:${s.sipTop ?? "??"} 지지:${s.sipBot ?? "??"}`)
      .join("\n");
    seunDaeunNote += `\n[세운별 십성 — 이 아이 일간 기준으로 미리 계산된 값. 그대로 사용하시오]\n${seunLines}\n`;

    if (input.daeun && input.daeun.length > 0) {
      const daeunLines = (input.daeun as { label: string; gz: string; krName?: string; yearStart: number; active: boolean; sipTop?: string; sipBot?: string }[])
        .map(d => `${d.yearStart}년~ ${d.krName ?? d.gz}대운 — 천간:${d.sipTop ?? "??"} 지지:${d.sipBot ?? "??"}${d.active ? " ★현재 대운" : ""}`)
        .join("\n");
      seunDaeunNote += `\n[대운별 십성]\n${daeunLines}\n`;
    }

    const phaseField = chapter === 7 ? "socialPhases" : "growthPhases";
    seunDaeunNote += `\n【중요】 ${phaseField} 5개의 yearStart/yearEnd는 2025~2034를 균등하게 나누시오. score·tone은 위 세운 십성을 근거로 산출하시오. desc 첫 문장은 반드시 "[yearStart]년 [간지독음]년과 [yearEnd]년 [간지독음]년은~" 형식으로 시작하시오. 한자 사용 금지.\n`;
  }

  const user = `아래는 ${honor}의 사주 명식입니다.

【호칭 규칙】 풀이 본문에서 이 아이를 지칭할 때는 반드시 "${honor}"라는 호칭을 사용하시오. "그", "그녀", "그대", "당신"은 절대 쓰지 마시오. 호칭이 반복되어 어색하면 "이 아이"로 대체하시오.

${input.manseryeokText}
${input.birthYear ? `\n출생연도: ${input.birthYear}년 / 현재연도: ${currentYear}년` : `\n현재연도: ${currentYear}년`}${ohaengCountNote}${seunDaeunNote}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
이번 장의 주제: ${theme}
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
${guide ? `\n작성 지침:\n${guide}\n` : ""}
위 명식을 꼼꼼히 분석하여, 아래 JSON 스키마를 정확히 채워주시오.
반드시 유효한 JSON만 출력하시오. 코드펜스(\`\`\`)나 설명 문장은 절대 쓰지 마시오.
풀이 대상은 부모가 의뢰한 자녀(아이)임을 염두에 두고, 부모가 읽는 시점에서 따뜻하고 실용적으로 서술하시오.

${schema}`;

  return { system: SYSTEM, user };
}
